    private void show_table(){
        int CC;
        
       // Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms","root","lalit@123");
                    
        //   Class.forName("com.mysql.jdbc.Driver");
        try{
            
    Connection con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/sms","root","lalit@123");
     PreparedStatement preparedStatement=con.prepareStatement("select * from addstudent");
     ResultSet rs=preparedStatement.executeQuery();
     ResultSetMetaData RSMD = rs.getMetaData();
     CC =RSMD.getColumnCount();
     DefaultTableModel DFT = (DefaultTableModel)jtable.getModel();
     
     DFT.setRowCount(0);
     
     while(rs.next()){
         Vector v2 = new Vector();
         
         for(int i =1;i<=CC;i++){
             v2.add(rs.getString("enrollno"));
             v2.add(rs.getString("firstname"));
             v2.add(rs.getString("lastname"));
             v2.add(rs.getString("dob"));
             v2.add(rs.getString("fathername"));
             v2.add(rs.getString("mothername"));
             v2.add(rs.getString("gender"));
             v2.add(rs.getString("coursetype"));
             v2.add(rs.getString("contact"));
             v2.add(rs.getString("address"));
         }
         
         DFT.addRow(v2);
     }
  
        }catch(SQLException e){
            System.out.println("Error fetching data: " + e.getMessage());
        }
    }// show table end
    
    
    
    
    
    
    
