    private void addstudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addstudentActionPerformed
        // add student details
  //------------------------------------------------------------------------------      
        String enrollno = jenrollno.getText();
        String firstname = jfirstname.getText();
        String lastname = jlastname.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("YYYY-MM-DD");
        String dob =dateFormat.format(jdob.getDate());
        String fathername = jfathername.getText();
        String mothername = jmothername.getText();
        
        String gender = "";
                if (jmale.isSelected()) {
                    gender = "Male";
                } else if (jfemale.isSelected()) {
                    gender = "Female";
                }
                
      String coursetype = jcoursetype.getText();
      String contact = jcontact.getText();
      String address = jaddress.getText();
      
        
        try{           
       Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/sms","root","lalit@123");
  
   String sql ="insert into addstudent (enrollno,firstname,lastname,dob,fathername,mothername,gender,coursetype,contact,address) values (?,?,?,?,?,?,?,?,?,?)";
  
    PreparedStatement preparedStatement=con.prepareStatement(sql);
    
    
    preparedStatement.setString(1,enrollno );
    preparedStatement.setString(2, firstname);
    preparedStatement.setString(3, lastname);
    preparedStatement.setString(4, dob);
    preparedStatement.setString(5, fathername);
    preparedStatement.setString(6, mothername);
    preparedStatement.setString(7, gender);
    preparedStatement.setString(8, coursetype);
    preparedStatement.setString(9, contact);
    preparedStatement.setString(10, address);
    
    
      int rowsInserted= preparedStatement.executeUpdate(); 
      
      if( rowsInserted>0){
       
       JOptionPane.showMessageDialog(this, "Registration Successful!");
        }
      else{
              JOptionPane.showMessageDialog(this, "Registration Failed!");
      
              }
      
      
       jenrollno.setText("");
       jfirstname.setText("");
       jlastname.setText("");
       jdob.setDate(null);
       jfathername.setText("");
       jmothername.setText("");
        gendergroup.clearSelection();
       jcoursetype.setText("");
       jcontact.setText("");
       jaddress.setText("");
    
    
        }catch(ClassNotFoundException | SQLException e){
            
            JOptionPane.showMessageDialog(this, "Connection Error");
      
        }
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_addstudentActionPerformed

